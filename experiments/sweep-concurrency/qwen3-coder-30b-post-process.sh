#!/usr/bin/env bash
set -euo pipefail

usage() {
  cat <<'EOF'
Batch post-process replay results generated by qwen3-coder-30b sweep runs.

Usage:
  bash experiments/sweep-concurrency/qwen3-coder-30b-post-process.sh [options]

Options:
  --root-dir <path>    Root replay results directory to scan.
                       Default: results/replay/qwen3-coder-30b
  --max-procs <n>      Number of worker processes.
                       Default: MAX_PROCS env var, else nproc, else 4
  --dry-run            Print discovered run directories and exit.
  -h, --help           Show this help message.
EOF
}

ROOT_DIR="results/replay/qwen3-coder-30b"
DRY_RUN=0

if [[ -n "${MAX_PROCS:-}" ]]; then
  MAX_PROCS_VALUE="$MAX_PROCS"
elif command -v nproc >/dev/null 2>&1; then
  MAX_PROCS_VALUE="$(nproc)"
else
  MAX_PROCS_VALUE="4"
fi

while [[ $# -gt 0 ]]; do
  case "$1" in
    --root-dir)
      if [[ $# -lt 2 ]]; then
        echo "Missing value for --root-dir" >&2
        exit 2
      fi
      ROOT_DIR="$2"
      shift 2
      ;;
    --max-procs)
      if [[ $# -lt 2 ]]; then
        echo "Missing value for --max-procs" >&2
        exit 2
      fi
      MAX_PROCS_VALUE="$2"
      shift 2
      ;;
    --dry-run)
      DRY_RUN=1
      shift
      ;;
    -h|--help)
      usage
      exit 0
      ;;
    *)
      echo "Unknown argument: $1" >&2
      usage >&2
      exit 2
      ;;
  esac
done

if ! [[ "$MAX_PROCS_VALUE" =~ ^[1-9][0-9]*$ ]]; then
  echo "--max-procs must be a positive integer: $MAX_PROCS_VALUE" >&2
  exit 2
fi

SCRIPT_DIR="$(cd -- "$(dirname -- "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd -- "$SCRIPT_DIR/../.." && pwd)"

cd "$REPO_ROOT"

if [[ ! -d "$ROOT_DIR" ]]; then
  echo "Replay root directory not found: $ROOT_DIR" >&2
  exit 1
fi

mapfile -t RUN_DIRS < <(
  find "$ROOT_DIR" \
    -type f \
    -path "*/sweep-concurrency/*/replay/summary.json" \
    -print \
    | LC_ALL=C sort \
    | while IFS= read -r summary_path; do
      dirname "$(dirname "$summary_path")"
    done
)

if (( ${#RUN_DIRS[@]} == 0 )); then
  echo "No sweep replay runs found under: $ROOT_DIR"
  exit 0
fi

if (( DRY_RUN == 1 )); then
  echo "Discovered ${#RUN_DIRS[@]} run directories under $ROOT_DIR"
  printf '%s\n' "${RUN_DIRS[@]}"
  exit 0
fi

NEEDS_VLLM_PROCESSING=0
for run_dir in "${RUN_DIRS[@]}"; do
  if [[ -d "$run_dir/vllm-log" ]]; then
    NEEDS_VLLM_PROCESSING=1
    break
  fi
done

if (( NEEDS_VLLM_PROCESSING == 1 )); then
  if ! python3 -c 'import prometheus_client' >/dev/null 2>&1; then
    echo "Missing python3 dependency: prometheus_client" >&2
    echo "Install it in the active environment, then rerun this script." >&2
    exit 1
  fi
fi

WORKER_SCRIPT_PATH="$(mktemp)"
cleanup() {
  rm -f "$WORKER_SCRIPT_PATH"
}
trap cleanup EXIT

cat >"$WORKER_SCRIPT_PATH" <<'EOF'
#!/usr/bin/env bash
set -euo pipefail

run_dir="$1"
did_work=0

if [[ -d "$run_dir/vllm-log" ]]; then
  python3 post-process/vllm-metrics/extract_run.py --run-dir "$run_dir" >/dev/null
  python3 post-process/vllm-metrics/summarize_timeseries.py --run-dir "$run_dir" >/dev/null
  did_work=1
fi

if [[ -d "$run_dir/gateway-output" ]]; then
  python3 post-process/gateway/llm-requests/extract_run.py --run-dir "$run_dir" >/dev/null
  did_work=1
fi

if (( did_work == 1 )); then
  printf '[done] %s\n' "$run_dir"
else
  printf '[skip] %s (no vllm-log/ or gateway-output/)\n' "$run_dir"
fi
EOF

chmod +x "$WORKER_SCRIPT_PATH"

echo "Discovered ${#RUN_DIRS[@]} run directories under $ROOT_DIR"
echo "Running post-processing with $MAX_PROCS_VALUE worker process(es)"

if ! printf '%s\0' "${RUN_DIRS[@]}" \
  | xargs -0 -n 1 -P "$MAX_PROCS_VALUE" "$WORKER_SCRIPT_PATH"; then
  echo "Post-processing failed for one or more run directories." >&2
  exit 1
fi

echo "Post-processing completed for ${#RUN_DIRS[@]} run directories."
